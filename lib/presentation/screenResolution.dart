  var _globalContainerHeight;
  var _topPositionGearContainer;
  var _leftPositionGearContainer;
  var _topPositionSocialIconContainer;
  var _leftPositionSocialIconContainer;

  double? get globalContainerWidth => _globalContainerWidth;

  set globalContainerWidth(double? value) {
    _globalContainerWidth = value;
  }

  double? get globalContainerHeight => _globalContainerHeight;

  set globalContainerHeight(double? value) {
    _globalContainerHeight = value;
  }

  double? get leftPositionGearContainer => _leftPositionGearContainer;

  set leftPositionGearContainer(double? value) {
    _leftPositionGearContainer = value;
  }

  double? get topPositionGearContainer => _topPositionGearContainer;

  set topPositionGearContainer(double? value) {
    _topPositionGearContainer = value;
  }

  double? get leftPositionSocialIconContainer => _leftPositionSocialIconContainer;

  set leftPositionSocialIconContainer(double? value) {
    _leftPositionSocialIconContainer = value;
  }

  double? get topPositionSocialIconContainer => _topPositionSocialIconContainer;

  set topPositionSocialIconContainer(double? value) {
    _topPositionSocialIconContainer = value;
  }
}